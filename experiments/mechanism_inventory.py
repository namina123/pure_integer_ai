def inventory_by_id() -> dict[str, MechanismRecord]:
    """按稳定 ID 返回台账副本。"""
    return {record.mechanism_id: record for record in MECHANISM_INVENTORY}


def readiness_candidates() -> tuple[MechanismRecord, ...]:
    """返回允许进入 readiness 计算的生产机制。"""
    return tuple(
        record for record in MECHANISM_INVENTORY
        if record.status == STATUS_PRODUCTION and record.readiness_eligible
    )


def inventory_json() -> list[dict[str, Any]]:
    """按台账声明顺序导出 JSON 兼容结构。"""
    return [record.to_json() for record in MECHANISM_INVENTORY]


def validate_inventory() -> tuple[str, ...]:
    """检查 ID、四态和 readiness 基本不变量。"""
    errors: list[str] = []
    seen: set[str] = set()
    for record in MECHANISM_INVENTORY:
        if record.mechanism_id in seen:
            errors.append(f"重复 mechanism_id: {record.mechanism_id}")
        seen.add(record.mechanism_id)
        if record.status not in VALID_STATUSES:
            errors.append(f"未知 status: {record.mechanism_id}={record.status}")
        if record.readiness_eligible and record.status != STATUS_PRODUCTION:
            errors.append(f"非生产机制不得进入 readiness: {record.mechanism_id}")
        if record.status in (STATUS_PRODUCTION, STATUS_OPT_IN) and not record.owner:
            errors.append(f"生产或 opt-in 机制缺 owner: {record.mechanism_id}")
        if record.status == STATUS_PRODUCTION and not record.readers:
            errors.append(f"生产机制缺 reader: {record.mechanism_id}")
    return tuple(errors)


